/*
    Using the switch case conditional statement to simplify the need for multiple 'if' statement
*/
var day = "Sunday"

switch (day) {
    case 'Monday':
        console.log('Do something')
        break;
    case 'Tuesday':
        console.log('Do something')
        break;
    case 'Wednesday':
        console.log('Do something')
        break;
    case 'Thursday':
        console.log('Do something')
        break;
    case 'Frdiday':
        console.log('Do something')
        break;
    case 'Saturday':
        console.log('Do something')
        break;
    case 'Sunday':
        console.log('Do something')
        break;
    default:
        console.log("There is no such day")
        break;
}